package dev.sixik.sdmtoolkit.ecs;

/**
 * Базовый контракт для всех компонентов сущности.
 * Компоненты должны быть максимально глупыми (Data Object) и хранить преимущественно состояние.
 */
public interface ECSBaseComponent {

    /**
     * Вызывается после добавления компонента в сущность.
     * Идеальное место для захвата ссылок на другие компоненты или кэширования значений.
     */
    default void init() { }

    /**
     * Определяет порядок инициализации компонентов.
     * Чем меньше число, тем раньше в списке окажется компонент и раньше вызовется его init().
     */
    default int priority() {
        return 0;
    }

    /**
     * Возвращает тип этого компонента (фабрику/сериализатор).
     */
    ECSComponentType<?> getType();

    /**
     * Определяет, на какой логической стороне существует компонент и нужно ли его синхронизировать.
     */
    default ECSSide getSide() {
        return ECSSide.Both;
    }

    @Deprecated
    default boolean dirty() {
        return false;
    }
}
